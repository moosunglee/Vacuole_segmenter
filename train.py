## 라이브러리 추가하기
import argparse

import os
import numpy as np

import torch
import torch.nn as nn
from torch.utils.data import DataLoader
from torch.utils.tensorboard import SummaryWriter

# from model import UNet
from models.unet3D_glob import Unet3D_glob
from model import *
from dataset import *
from util import save, load
from loss import *
import torchvision

import matplotlib.pyplot as plt

from torchvision import transforms, datasets

## Parser 생성하기
parser = argparse.ArgumentParser(description="Train the UNet",
                                 formatter_class=argparse.ArgumentDefaultsHelpFormatter)

parser.add_argument("--lr", default=1e-3, type=float, dest="lr")
parser.add_argument("--batch_size", default=4, type=int, dest="batch_size")
parser.add_argument("--num_epoch", default=100, type=int, dest="num_epoch")

parser.add_argument("--data_dir", default="./datasets", type=str, dest="data_dir")
parser.add_argument("--ckpt_dir", default="./checkpoint", type=str, dest="ckpt_dir")
parser.add_argument("--log_dir", default="./log", type=str, dest="log_dir")
parser.add_argument("--result_dir", default="./result", type=str, dest="result_dir")

parser.add_argument("--mode", default="train", type=str, dest="mode")
parser.add_argument("--train_continue", default="off", type=str, dest="train_continue")

args = parser.parse_args()

## 트레이닝 파라메터 설정하기
lr = args.lr
batch_size = args.batch_size
num_epoch = args.num_epoch

data_dir = args.data_dir
ckpt_dir = args.ckpt_dir
log_dir = args.log_dir
result_dir = args.result_dir

mode = args.mode
train_continue = args.train_continue

device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')

print("learning rate: %.4e" % lr)
print("batch size: %d" % batch_size)
print("number of epoch: %d" % num_epoch)
print("data dir: %s" % data_dir)
print("ckpt dir: %s" % ckpt_dir)
print("log dir: %s" % log_dir)
print("result dir: %s" % result_dir)
print("mode: %s" % mode)

## 디렉토리 생성하기
if not os.path.exists(result_dir):
    os.makedirs(os.path.join(result_dir, 'png'))
    os.makedirs(os.path.join(result_dir, 'numpy'))

## 네트워크 학습하기
if mode == 'train':
    transform = transforms.Compose([RandomFlip(),RandomResize(), RandomRotation_XY(), RandomCrop(), ToTensor()])
    # transform = transforms.Compose([Normalization(mean=0.5, std=0.5), RandomFlip(),RandomResize(), RandomRotation_XY(), RandomCrop(), ToTensor()])

    dataset_train = Dataset(data_dir=os.path.join(data_dir, 'train'), transform=transform)
    loader_train = DataLoader(dataset_train, batch_size=batch_size, shuffle=True, num_workers=8)

    dataset_val = Dataset(data_dir=os.path.join(data_dir, 'val'), transform=transform)
    loader_val = DataLoader(dataset_val, batch_size=batch_size, shuffle=False, num_workers=8)

    # 그밖에 부수적인 variables 설정하기
    num_data_train = len(dataset_train)
    num_data_val = len(dataset_val)

    num_batch_train = np.ceil(num_data_train / batch_size)
    num_batch_val = np.ceil(num_data_val / batch_size)
else:
    # transform = transforms.Compose([Normalization(mean=0.5, std=0.5), ToTensor()])
    transform = transforms.Compose([ToTensor()])

    dataset_test = Dataset(data_dir=os.path.join(data_dir, 'test'), transform=transform)
    loader_test = DataLoader(dataset_test, batch_size=batch_size, shuffle=False, num_workers=8)

    # 그밖에 부수적인 variables 설정하기
    num_data_test = len(dataset_test)
    num_batch_test = np.ceil(num_data_test / batch_size)

## 네트워크 생성하기
net = UNet().to(device)
# net = Unet3D_glob(feature_scale=1, trans_feature=64).to(device)

## 손실함수 정의하기
# fn_loss1 = nn.BCEWithLogitsLoss().to(device)
# fn_loss2 = nn.CrossEntropyLoss().to(device)
# fn_loss3 = nn.BCELoss().to(device)
# fn_loss = nn.BCEWithLogitsLoss().to(device)
# fn_loss = IoULoss()
fn_loss1 = IoULoss()
fn_loss2 = FocalTverskyLoss()
# fn_loss3 = nn.BCEWithLogitsLoss().to(device)
fn_loss4 = nn.BCEWithLogitsLoss().to(device)
## Optimizer 설정하기
optim = torch.optim.Adam(net.parameters(), lr=lr)

## 그밖에 부수적인 functions 설정하기
fn_tonumpy = lambda x: x.to('cpu').detach().numpy().transpose(0, 2, 3, 4, 1)
fn_denorm = lambda x, mean, std: (x * std) + mean
fn_class = lambda x: 1.0 * (x > 0.5)

## Tensorboard 를 사용하기 위한 SummaryWriter 설정
writer_train = SummaryWriter(log_dir=os.path.join(log_dir, 'train'))
writer_val = SummaryWriter(log_dir=os.path.join(log_dir, 'val'))

## 네트워크 학습시키기
st_epoch = 0

# TRAIN MODE
if mode == 'train':
    if train_continue == "on":
        net, optim, st_epoch = load(ckpt_dir=ckpt_dir, net=net, optim=optim)

    for epoch in range(st_epoch + 1, num_epoch + 1):
        net.train()
        loss_arr = []

        for batch, data in enumerate(loader_train, 1):
            # forward pass
            label = data['label'].to(device)
            input_ = data['input'].to(device)

            output = net(input_)

            # backward pass
            optim.zero_grad()

            # loss = 0.15*fn_loss1(output, label) + 0.15*fn_loss2(output, label) + 0.5*fn_loss3(output, label)+0.2*fn_loss4(output, label)
            loss = 0.9*fn_loss4(output, label) + 0.1*fn_loss2(output, label)
            # loss = fn_loss(output,label)
            loss.backward()

            optim.step()

            # 손실함수 계산
            loss_arr += [loss.item()]

            print("TRAIN: EPOCH %04d / %04d | BATCH %04d / %04d | LOSS %.4f" %
                  (epoch, num_epoch, batch, num_batch_train, np.mean(loss_arr)))

            # Tensorboard 저장하기
            label = fn_tonumpy(fn_class(label))
            # input_ = fn_tonumpy(fn_denorm(input_, mean=0.5, std=0.5))
            input_ = fn_tonumpy(input_)
            output = fn_tonumpy(fn_class(output))

            writer_train.add_image('label', np.max(label,axis=1), num_batch_train * (epoch - 1) + batch, dataformats='NHWC')
            writer_train.add_image('input', np.max(input_,axis=1), num_batch_train * (epoch - 1) + batch, dataformats='NHWC')
            writer_train.add_image('output', np.max(output,axis=1), num_batch_train * (epoch - 1) + batch, dataformats='NHWC')

        writer_train.add_scalar('loss', np.mean(loss_arr), epoch)

        with torch.no_grad():
            net.eval()
            loss_arr = []

            for batch, data in enumerate(loader_val, 1):
                # forward pass
                label = data['label'].to(device)
                input_ = data['input'].to(device)

                output = net(input_)

                # 손실함수 계산하기
                # loss = 0.15*fn_loss1(output, label) + 0.15*fn_loss2(output, label) + 0.5*fn_loss3(output, label)+0.2*fn_loss4(output, label)
                loss = 0.9*fn_loss4(output, label) + 0.1*fn_loss2(output, label)
                # loss = fn_loss(output,label)

                loss_arr += [loss.item()]

                print("VALID: EPOCH %04d / %04d | BATCH %04d / %04d | LOSS %.4f" %
                      (epoch, num_epoch, batch, num_batch_val, np.mean(loss_arr)))

                # Tensorboard 저장하기
                label = fn_tonumpy(fn_class(label))
                input_ = fn_tonumpy(input_)
                output = fn_tonumpy(fn_class(output))

                writer_val.add_image('label', np.max(label,axis=1), num_batch_val * (epoch - 1) + batch, dataformats='NHWC')
                writer_val.add_image('input', np.max(input_,axis=1), num_batch_val * (epoch - 1) + batch, dataformats='NHWC')
                writer_val.add_image('output', np.max(output,axis=1), num_batch_val * (epoch - 1) + batch, dataformats='NHWC')

        writer_val.add_scalar('loss', np.mean(loss_arr), epoch)

        if epoch % 50 == 0:
            save(ckpt_dir=ckpt_dir, net=net, optim=optim, epoch=epoch)

    writer_train.close()
    writer_val.close()

# TEST MODE
else:
    net, optim, st_epoch = load(ckpt_dir=ckpt_dir, net=net, optim=optim)

    with torch.no_grad():
        net.eval()
        loss_arr = []
        id = 0

        for batch, data in enumerate(loader_test, 1):
            # forward pass
            label = data['label'].to(device)
            input_ = data['input'].to(device)

            output = net(input_)

            # 손실함수 계산하기
            # loss = 0.15*fn_loss1(output, label) + 0.15*fn_loss2(output, label) + 0.5*fn_loss3(output, label)+0.2*fn_loss4(output, label)
            loss = 0.9*fn_loss4(output, label) + 0.1*fn_loss2(output, label)
            # loss = fn_loss(output,label)

            loss_arr += [loss.item()]

            print("TEST: BATCH %04d / %04d | LOSS %.4f" %
                  (batch, num_batch_test, np.mean(loss_arr)))

            # Tensorboard 저장하기
            label = fn_tonumpy(fn_class(label))
            input_ = fn_tonumpy(input_)
            output = fn_tonumpy(fn_class(output))

            for j in range(label.shape[0]):

                plt.imsave(os.path.join(result_dir, 'png', 'label_%04d.png' % id), np.max(label[j].squeeze(),axis=0).squeeze(), cmap='gray')
                plt.imsave(os.path.join(result_dir, 'png', 'input_%04d.png' % id), np.max(input_[j].squeeze(),axis=0).squeeze(), cmap='gray')
                plt.imsave(os.path.join(result_dir, 'png', 'output_%04d.png' % id), np.max(output[j].squeeze(),axis=0).squeeze(), cmap='gray')

                np.save(os.path.join(result_dir, 'numpy', 'label_%04d.npy' % id), label[j].squeeze())
                np.save(os.path.join(result_dir, 'numpy', 'input_%04d.npy' % id), input_[j].squeeze())
                np.save(os.path.join(result_dir, 'numpy', 'output_%04d.npy' % id), output[j].squeeze())
                id += 1

    print("AVERAGE TEST: BATCH %04d / %04d | LOSS %.4f" %
          (batch, num_batch_test, np.mean(loss_arr)))

